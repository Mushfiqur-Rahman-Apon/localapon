def Goto(x, y):
    penup()
    goto(x, y)
    pendown()
Goto(-100, 250)
def iphon (x, y):
    for i in range (2):
        fd(x)
        for i in range (90):
            fd(0.5)
            rt(1)
        fd(y)
        for i in range (90):
            fd(0.5)
            rt(1)  
begin_fill()
pencolor("#909090")
fillcolor("#ffffff")
iphon(200, 450)
end_fill()
Goto(-90, 240)
